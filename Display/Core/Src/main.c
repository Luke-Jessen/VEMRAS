void changDir(uint8_t dir){
	TIM2->CR1 &= 0xFFFE;
	//TIM8->CR1 &= 0xFFFE;

	if(dir == 1){
		TIM1->CNT = 0;
		TIM8->CNT = 2;

		TIM1->CCR3 = 4;
		TIM1->CCR4 = 2;

		TIM8->CCR1 = 2;
		TIM8->CCR2 = 4;


	}else{
		TIM1->CNT = 2;
		TIM8->CNT = 0;

		TIM1->CCR3 = 2;
		TIM1->CCR4 = 4;

		TIM8->CCR1 = 4;
		TIM8->CCR2 = 2;

	}




	TIM2->CR1 |= 0x0001;
	//TIM8->CR1 |= 0x0001;
}

